# ოპერატორი: >
print(10 > 5)    # True 
print(3 > 8)     # False 
print(-2 > -5)   # True
print(0 > 0)     # False 
print(7.5 > 7.49) # True 

# ოპერატორი: >=
print(10 >= 10)  # True
print(5 >= 9)    # False 
print(-3 >= -3)  # True 
print(4.1 >= 4)  # True 
print(2 >= 3)    # False 

# ოპერატორი: <
print(1 < 5)     # True 
print(9 < 2)     # False
print(-10 < -5)  # True 
print(3.14 < 3.15) # True 
print(0 < 0)     # False 

# ოპერატორი: <=
print(4 <= 4)    # True 
print(7 <= 3)    # False 
print(-1 <= 0)   # True 
print(2.5 <= 2.5) # True
print(100 <= 99) # False 

# ოპერატორი: ==
print(5 == 5)    # True 
print(3 == 8)    # False 
print(-2 == -2)  # True
print(7 == 7) # True 
print(0 == 1)    # False 

# ოპერატორი: !=
print(5 != 3)    # True 
print(4 != 4)    # False 
print(-1 != 1)   # True 
print(0 != 0)    # False 